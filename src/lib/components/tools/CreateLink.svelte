<script>
  import { classNames } from '$lib/util';
  import { toggleMark } from 'prosemirror-commands';
  import { createLink } from '$lib/prosemirrorCommands';

  export let editorView;
  export let editorState;

  $: schema = editorState.schema;
  $: disabled = !createLink(editorState, null, editorView);

  function handleClick() {
    let url = prompt('Ange l√§nkens URL', 'https://example.com');
    if (url) {
      toggleMark(schema.marks.link, { href: url })(editorState, editorView.dispatch);
      editorView.focus();
    }
  }
</script>

<button
  on:click={handleClick}
  {disabled}
  class={classNames(
    'text-white hover:bg-white hover:text-black',
    'sm:mx-1 rounded-full p-2 disabled:opacity-30'
  )}
>
  <slot />
</button>
